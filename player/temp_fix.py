        # –ù–æ –Ω–∞–º –Ω—É–∂–µ–Ω ID —Ç–µ–∫—É—â–µ–≥–æ —Ñ–∏–ª—å–º–∞
        output_lines.append(line)  # –ü–æ–∫–∞ –æ—Å—Ç–∞–≤–ª—è–µ–º –∫–∞–∫ –µ—Å—Ç—å
